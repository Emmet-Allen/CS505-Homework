import java.lang.Math;

// Program that outputs the area of a circle
public class CircleArea {
    public static void main (String[] args){
 final int r = 2;
 double Pi = Math.PI;
 final double calculation = (Pi * (r*r) );
 System.out.println("The area of a circle is with a radius of " + r + " is: " + calculation);
    }
}
